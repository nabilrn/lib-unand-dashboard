export type Dictionary = typeof dictionaries[typeof defaultLocale];

export const getDictionary = (locale: LocaleCode): Dictionary => dictionaries[locale] as Dictionary;

const browserLocaleMap: Record<LocaleCode, string> = {
  id: 'id-ID',
  en: 'en-US',
  zh: 'zh-CN',
  ar: 'ar-SA',
};

export const toBrowserLocale = (locale: LocaleCode) => browserLocaleMap[locale];

export function interpolate(template: string, values: InterpolateValues = {}) {
  return template.replace(/\{(\w+)\}/g, (_, key) => String(values[key] ?? `{${key}}`));
}
